namespace Webkul\MarketplacePreorder\Model\ResourceModel\PreorderItems;

use \Webkul\MarketplacePreorder\Model\ResourceModel\AbstractCollection;

/**
 * Webkul MarketplacePreorder ResourceModel Seller collection
 */
class Collection extends AbstractCollection
{
    /**
     * @var string
     */
    protected $_idFieldName = 'id';

    /**
     * Define resource model
     *
     * @return void
     */
    protected function _construct()
    {
        $this->_init(
            'Webkul\MarketplacePreorder\Model\PreorderItems',
            'Webkul\MarketplacePreorder\Model\ResourceModel\PreorderItems'
        );
    }

    
    /**
     * Add filter by store
     *
     * @param int|array|\Magento\Store\Model\Store $store
     * @param bool $withAdmin
     * @return $this
     */
    public function addStoreFilter($store, $withAdmin = true)
    {
        if (!$this->getFlag('store_filter_added')) {
            $this->performAddStoreFilter($store, $withAdmin);
        }
        return $this;
    }
}
